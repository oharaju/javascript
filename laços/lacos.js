//Estrutura com Teste Lógico no Início
var c = 1
console.log('Vai começar')
while (c <= 6) {
    console.log(`Passo ${c}`)
    c++
}
console.log('FIM')



//Estrutura com Teste Lógico no Final
var c = 1
do {
    console.log(`Passo ${c}`)
    c++

} while (c <= 6)



// Usando For
console.log ('Vai começar..')
for (var c = 1; c <= 6; c++) {
    console.log(c)
}

console.log('FIM')